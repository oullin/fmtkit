func TestCollectGoFilesAppliesConfiguredExclusionsAndDeduplicates(t *testing.T) {
	root := t.TempDir()
	keep := filepath.Join(root, "keep.go")
	duplicate := filepath.Join(root, "nested", "duplicate.go")
	testutil.WriteGoFile(t, keep, "package sample\n")
	testutil.WriteGoFile(t, duplicate, "package sample\n")
	testutil.WriteGoFile(t, filepath.Join(root, "skip_name.go"), "package sample\n")
	testutil.WriteGoFile(t, filepath.Join(root, "blocked", "skip_path.go"), "package sample\n")
	testutil.WriteFile(t, filepath.Join(root, "not_go.txt"), "package sample\n")
	testutil.WriteGoFile(t, filepath.Join(root, "generated_header.go"), "// Code generated by test. DO NOT EDIT.\npackage sample\n")

	cfg := config.Default()
	cfg.NotName = []string{"skip_*"}
	cfg.NotPath = []string{"blocked/"}

	files, err := engine.CollectGoFiles([]string{root, keep, duplicate}, cfg)

	if err != nil {
		t.Fatalf("collect files: %v", err)
	}

	if len(files) != 2 {
		t.Fatalf("expected 2 files, got %d: %#v", len(files), files)
	}

	for _, want := range []string{keep, duplicate} {
		abs, err := filepath.Abs(want)

		if err != nil {
			t.Fatalf("abs: %v", err)
		}

		found := false

		for _, got := range files {
			if got == abs {
				found = true

				break
			}
		}

		if !found {
			t.Fatalf("expected collected files to include %s: %#v", abs, files)
		}
	}
}

