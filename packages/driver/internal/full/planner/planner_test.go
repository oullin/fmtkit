func TestBuildSelectsEveryChangedGitState(t *testing.T) {
	dir := initRepo(t)
	writeFile(t, filepath.Join(dir, ".gitignore"), "ignored.go\n")
	writeFile(t, filepath.Join(dir, "staged.go"), "package sample\n")
	writeFile(t, filepath.Join(dir, "unstaged.ts"), "const value = 1;\n")
	writeFile(t, filepath.Join(dir, "old.ts"), "const old = true;\n")
	writeFile(t, filepath.Join(dir, "deleted.go"), "package sample\n")
	writeFile(t, filepath.Join(dir, "notes.md"), "before\n")
	run(t, dir, "git", "add", ".")
	run(t, dir, "git", "commit", "-qm", "initial")

	writeFile(t, filepath.Join(dir, "staged.go"), "package sample\n\nconst staged = true\n")
	run(t, dir, "git", "add", "staged.go")
	writeFile(t, filepath.Join(dir, "unstaged.ts"), "const value = 2;\n")
	run(t, dir, "git", "mv", "old.ts", "renamed.ts")
	run(t, dir, "git", "rm", "-q", "deleted.go")
	writeFile(t, filepath.Join(dir, "untracked file.vue"), "<script setup lang=\"ts\"></script>\n")
	writeFile(t, filepath.Join(dir, "ignored.go"), "package ignored\n")
	writeFile(t, filepath.Join(dir, "notes.md"), "after\n")

	plan, err := Build(Options{Cwd: dir})

	if err != nil {
		t.Fatalf("build plan: %v", err)
	}

	want := Plan{
		Go: []string{"staged.go"},
		TS: []string{"renamed.ts", "unstaged.ts", "untracked file.vue"},
	}

	if !reflect.DeepEqual(plan, want) {
		t.Fatalf("plan mismatch\nwant: %#v\n got: %#v", want, plan)
	}
}

