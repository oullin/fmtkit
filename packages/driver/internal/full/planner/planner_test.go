	if err != nil {
		t.Fatalf("resolve user cache: %v", err)
	}

	writeFile(t, filepath.Join(home, "sample.ts"), "const value = 1;\n")
	writeFile(t, filepath.Join(cacheRoot, "go-fmt", "contained", "runtime", "bundled.go"), "package runtime\n")
