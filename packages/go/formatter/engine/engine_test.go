func TestProcessFileReportsReadRuleAndFormatterErrors(t *testing.T) {
	root := t.TempDir()
	path := filepath.Join(root, "sample.go")
	testutil.WriteGoFile(t, path, "package sample\n")

	tests := []struct {
		name       string
		files      []string
		rules      []rules.Rule
		formatters []engine.Formatter
		wantError  string
	}{
		{
			name:      "read error",
			files:     []string{filepath.Join(root, "missing.go")},
			wantError: "read file:",
		},
		{
			name:      "rule error",
			files:     []string{path},
			rules:     []rules.Rule{errorRule{}},
			wantError: "broken-rule: rule failed",
		},
		{
			name:       "formatter error",
			files:      []string{path},
			formatters: []engine.Formatter{errorFormatter{}},
			wantError:  "broken-formatter: formatter failed",
		},
	}

	for _, tt := range tests {
		tt := tt

		t.Run(tt.name, func(t *testing.T) {
			report, err := engine.New(config.Default(), tt.rules, tt.formatters).CheckFiles(context.Background(), tt.files)

			if err != nil {
				t.Fatalf("check files: %v", err)
			}

			if report.Result != "fail" {
				t.Fatalf("expected fail result, got %q", report.Result)
			}

			if len(report.Results) != 1 {
				t.Fatalf("expected one result, got %#v", report.Results)
			}

			if !strings.Contains(report.Results[0].Error, tt.wantError) {
				t.Fatalf("expected error containing %q, got %q", tt.wantError, report.Results[0].Error)
			}
		})
	}
}

func TestFormatFilesReportsWriteErrors(t *testing.T) {
	if os.Geteuid() == 0 {
		t.Skip("root bypasses file permissions, so the read-only file stays writable")
	}

	root := t.TempDir()
	path := filepath.Join(root, "sample.go")
	testutil.WriteGoFile(t, path, "package sample\n")

	// The engine replaces files atomically via a sibling temp file, so a
	// read-only directory is what makes the write fail: the temp file can
	// never be created next to the source.
	if err := os.Chmod(root, 0o555); err != nil {
		t.Fatalf("chmod readonly: %v", err)
	}

	t.Cleanup(func() {
		_ = os.Chmod(root, 0o755)
	})

	report, err := engine.New(config.Default(), nil, []engine.Formatter{rewriteFormatter{}}).FormatFiles(context.Background(), []string{path})

	if err != nil {
		t.Fatalf("format files: %v", err)
	}

	if report.Result != "fail" {
		t.Fatalf("expected fail result, got %q", report.Result)
	}

	if len(report.Results) != 1 || !strings.Contains(report.Results[0].Error, "write file:") {
		t.Fatalf("expected write error result, got %#v", report.Results)
	}
}

