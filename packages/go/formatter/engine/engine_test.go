type gofmtFormatter struct{}

type goimportsFormatter struct{}

type errorRule struct{}

type errorFormatter struct{}

type rewriteFormatter struct{}

func defaultRules() []rules.Rule {
	return []rules.Rule{spacing.New()}
}

func (gofmtFormatter) Name() string {
	return "gofmt"
}

func (gofmtFormatter) Format(src []byte) ([]byte, error) {
	return format.Source(src)
}

func (goimportsFormatter) Name() string {
	return "goimports"
}

func (goimportsFormatter) Format(src []byte) ([]byte, error) {
	return imports.Process("", src, nil)
}

func (errorRule) Name() string {
	return "broken-rule"
}

func (errorRule) Apply(string, []byte) ([]rules.Violation, []byte, error) {
	return nil, nil, errors.New("rule failed")
}

func (errorFormatter) Name() string {
	return "broken-formatter"
}

func (errorFormatter) Format([]byte) ([]byte, error) {
	return nil, errors.New("formatter failed")
}

func (rewriteFormatter) Name() string {
	return "rewrite"
}

func (rewriteFormatter) Format([]byte) ([]byte, error) {
	return []byte("package sample\n\nfunc rewritten() {}\n"), nil
}

func defaultFormatters() []engine.Formatter {
	return []engine.Formatter{gofmtFormatter{}, goimportsFormatter{}}
}

